export function getCitiesByCountry(countryKey: string): City[] {
  return CITIES_DATABASE[countryKey] || [];
}

export function getCityCoordinates(countryKey: string, cityName: string): { latitude: number; longitude: number } | null {
  const cities = CITIES_DATABASE[countryKey];
  if (!cities) return null;

  const city = cities.find(c => c.name === cityName);
  if (!city) return null;

  return {
    latitude: city.latitude,
    longitude: city.longitude
  };
}

export function searchCities(searchTerm: string, countryKey: string): City[] {
  const cities = CITIES_DATABASE[countryKey];
  if (!cities || !searchTerm) return cities || [];

  const normalizedSearch = searchTerm.trim().toLowerCase();
  return cities.filter(city =>
    city.name.toLowerCase().includes(normalizedSearch)
  );
}
